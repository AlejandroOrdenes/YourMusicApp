    private void btnGuardarAgreMusicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarAgreMusicaActionPerformed

        // Rescatar y validar
        validarDatos();
        String nombreCancion;
        String estilo;
        String artistaCancion;
        String album;
        String archivoCancion;

        // Rescatar y validar
        nombreCancion = txtNombreCancion.getText();
        estilo = jComboEstilo.getSelectedItem().toString();
        artistaCancion = txtArtistaCancion.getText();
        album = txtTituloAlbum.getText();
        archivoCancion = file;

        // Crear objeto
        Cancion cancion1 = new Cancion(nombreCancion, estilo, artistaCancion, album, archivoCancion);

        // Guardar en BD
        Registro reg = new Registro();
        if (reg.buscarNombreCancion(nombreCancion).size() == 0 && reg.buscarArtistaCancion(artistaCancion).size() == 0) {
            if (reg.agregarCancion(cancion1)) {
                JOptionPane.showMessageDialog(this, "Se agrego Cancion " + nombreCancion, "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                txtNombreCancion.setText("");
                txtArtistaCancion.setText("");
                txtTituloAlbum.setText("");
                jtRuta.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "NO se agrego Cancion " + nombreCancion, "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "La cancion ya existe " + nombreCancion, "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarAgreMusicaActionPerformed

    private void jButtonAbrirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAbrirArchivoActionPerformed

        JFileChooser dlg = new JFileChooser();
        int option = dlg.showOpenDialog(this);
        if (option == JFileChooser.APPROVE_OPTION) {
            file = dlg.getSelectedFile().getPath().toString();
            System.out.println(file);
        }

        jtRuta.setText(file);
        validarDatos();
        if (jtRuta.getText().isEmpty()) {
            lblarchivo.setText("*Campo Requerido");
        } else {
            if(jtRuta.getText().contains(".mp3") || jtRuta.getText().contains(".wav")){
                lblarchivo.setText("");
            }else{
                lblarchivo.setText("*Campo Requerido");
                JOptionPane.showMessageDialog(this, "Extension del Archivo incompatible", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        }

       
        

    

    }//GEN-LAST:event_jButtonAbrirArchivoActionPerformed

