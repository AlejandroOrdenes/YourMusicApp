    public void validarDatos() {
        
        if (txtNombreCancion.getText().isEmpty()) {
            lblnombre.setText("*Campo Requerido");
        } else {
            lblnombre.setText("");
        }

        if (txtArtistaCancion.getText().isEmpty()) {
            lblartista.setText("*Campo Requerido");
        } else {
            lblartista.setText("");
        }

        if (txtTituloAlbum.getText().isEmpty()) {
            lblalbum.setText("*Campo Requerido");
        } else {
            lblalbum.setText("");
        }
        
         
        

        if (txtNombreCancion.getText().isEmpty() || txtArtistaCancion.getText().isEmpty() || txtTituloAlbum.getText().isEmpty() || jtRuta.getText().isEmpty()) {
            btnGuardarAgreMusica.setEnabled(false);
        } else {
            if(jtRuta.getText().contains(".mp3") || jtRuta.getText().contains(".wav")) {
                btnGuardarAgreMusica.setEnabled(true);
           
            }
            
        }
    
        
    }

